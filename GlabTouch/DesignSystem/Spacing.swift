import SwiftUI

enum Spacing {
    /// 4pt — inline element gaps, tight vertical stacking
    static let xs: CGFloat = 4
    /// 8pt — standard content spacing, row internals
    static let sm: CGFloat = 8
    /// 12pt — related group separation, horizontal icon-to-text
    static let md: CGFloat = 12
    /// 16pt — section internal padding, default system padding
    static let lg: CGFloat = 16
    /// 24pt — section-to-section separation
    static let xl: CGFloat = 24
}
